package app

import (
	"github.com/gin-gonic/gin"
	"github.com/mrokoo/goERP/internal/system/role/domain"
)

type RoleHandler struct {
	roleService RoleServcie
}

func NewRoleHandler(roleService RoleServcie) *RoleHandler {
	return &RoleHandler{
		roleService: roleService,
	}
}

func (h *RoleHandler) GetRoleList(ctx *gin.Context) {
	roles, err := h.roleService.GetRoleList()
	if err != nil {
		ctx.JSON(400, gin.H{
			"code":     -1,
			"showMsg":  "failure",
			"errorMsg": err.Error(),
			"data":     nil,
		})
		return
	}

	ctx.JSON(200, gin.H{
		"code":     1,
		"showMsg":  "success",
		"errorMsg": "",
		"data":     roles,
	})
}

func (h *RoleHandler) AddRole(ctx *gin.Context) {
	var req domain.Role
	if err := ctx.ShouldBindJSON(&req); err != nil {
		ctx.JSON(400, gin.H{
			"code":     -1,
			"showMsg":  "failure",
			"errorMsg": err.Error(),
			"data":     nil,
		})
		return
	}

	err := h.roleService.AddRole(&req)
	if err != nil {
		ctx.JSON(400, gin.H{
			"code":     -1,
			"showMsg":  "failure",
			"errorMsg": err.Error(),
			"data":     nil,
		})
		return
	}

	ctx.JSON(200, gin.H{
		"code":     1,
		"showMsg":  "success",
		"errorMsg": "",
		"data":     nil,
	})
}

